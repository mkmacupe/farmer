  }

  return $values
}

function Invoke-CmdWithTimeout {
  param(
    [string]$CommandLine,
    [int]$TimeoutSeconds = 30,
    [switch]$Quiet
  )

  $stdoutPath = [System.IO.Path]::GetTempFileName()
  $stderrPath = [System.IO.Path]::GetTempFileName()
  try {
    $process = Start-Process `
      -FilePath "cmd.exe" `
      -ArgumentList "/c", $CommandLine `
      -RedirectStandardOutput $stdoutPath `
      -RedirectStandardError $stderrPath `
      -PassThru

    $completed = $process.WaitForExit($TimeoutSeconds * 1000)
    if (-not $completed) {
      try {
        $process.Kill($true)
      } catch {
        # Ignore kill failures on already-exited processes.
      }
    } else {
      $process.WaitForExit()
      $process.Refresh()
    }

    $stdout = if (Test-Path -LiteralPath $stdoutPath) { Get-Content -LiteralPath $stdoutPath -Raw } else { "" }
    $stderr = if (Test-Path -LiteralPath $stderrPath) { Get-Content -LiteralPath $stderrPath -Raw } else { "" }
    $output = @($stdout, $stderr) -join ""

    if (-not $Quiet -and -not [string]::IsNullOrWhiteSpace($output)) {
      $output.TrimEnd() | Out-Host
    }

    return [pscustomobject]@{
      ExitCode = if ($completed) { $process.ExitCode } else { $null }
      TimedOut = -not $completed
      Output = $output
    }
  } finally {
    Remove-Item -LiteralPath $stdoutPath -Force -ErrorAction SilentlyContinue
    Remove-Item -LiteralPath $stderrPath -Force -ErrorAction SilentlyContinue
  }
}

function Test-DockerReady {
  try {
    $serverVersion = docker version --format "{{.Server.Version}}" 2>$null
    return $LASTEXITCODE -eq 0 -and -not [string]::IsNullOrWhiteSpace(($serverVersion | Out-String))
  } catch {
    return $false
  }
}

function Start-DockerDesktopIfNeeded {
  param([int]$TimeoutSeconds = 60)

  if (Test-DockerReady) {
    Write-Host "Docker daemon is ready."
    return
  }

  Write-Warning "Docker daemon is not ready. Trying to start Docker Desktop..."

  $dockerDesktopPath = $null
  $candidatePaths = @(
    (Join-Path $env:ProgramFiles "Docker\Docker\Docker Desktop.exe"),
    (Join-Path $env:ProgramW6432 "Docker\Docker\Docker Desktop.exe"),
    (Join-Path $env:LocalAppData "Docker\Docker\Docker Desktop.exe")
  ) | Where-Object { -not [string]::IsNullOrWhiteSpace($_) }

  foreach ($candidate in $candidatePaths) {
    if (Test-Path -LiteralPath $candidate) {
      $dockerDesktopPath = $candidate
      break
    }
  }

  if ($dockerDesktopPath) {
    try {
      $existingDesktop = Get-Process -Name "Docker Desktop" -ErrorAction SilentlyContinue
      if (-not $existingDesktop) {
        Start-Process -FilePath $dockerDesktopPath | Out-Null
        Write-Host "Starting Docker Desktop: $dockerDesktopPath"
      } else {
        Write-Host "Docker Desktop process is already running."
      }
    } catch {
      Write-Warning "Failed to start Docker Desktop automatically: $($_.Exception.Message)"
    }
  } else {
    Write-Warning "Docker Desktop executable not found. Start it manually and keep this window open."
  }

  $deadline = (Get-Date).AddSeconds($TimeoutSeconds)
  $nextHeartbeat = Get-Date
  while ((Get-Date) -lt $deadline) {
    if (Test-DockerReady) {
      Write-Host "Docker daemon is ready."
      return
    }
    if ((Get-Date) -ge $nextHeartbeat) {
      Write-Host "Waiting for Docker daemon..."
      $nextHeartbeat = (Get-Date).AddSeconds(10)
    }
    Start-Sleep -Seconds 2
  }

  throw "Docker daemon is not ready after $TimeoutSeconds seconds. Open Docker Desktop, wait for engine startup, and run the script again."
}

function Test-PortAvailable {
  param([int]$Port)

  $inUse = Get-NetTCPConnection -State Listen -LocalPort $Port -ErrorAction SilentlyContinue
  if ($inUse) {
    return $false
  }

  try {
    $listenerV4 = [System.Net.Sockets.TcpListener]::new([System.Net.IPAddress]::Loopback, $Port)
    $listenerV6 = [System.Net.Sockets.TcpListener]::new([System.Net.IPAddress]::IPv6Loopback, $Port)
    $listenerV4.Start()
    $listenerV6.Start()
    $listenerV4.Stop()
    $listenerV6.Stop()
    return $true
  } catch {
    if ($listenerV4) {
      try { $listenerV4.Stop() } catch {}
    }
    if ($listenerV6) {
      try { $listenerV6.Stop() } catch {}
    }
    return $false
  }
}

function Resolve-Port {
  param(
    [int]$RequestedPort,
    [string]$Label
  )

  if (Test-PortAvailable -Port $RequestedPort) {
    return $RequestedPort
  }

  if ($StrictPorts) {
    throw "$Label port $RequestedPort is busy. Use a different port."
  }

  for ($candidate = $RequestedPort + 1; $candidate -le $RequestedPort + 50; $candidate++) {
    if (Test-PortAvailable -Port $candidate) {
      Write-Warning "$Label port $RequestedPort is busy. Using $candidate instead."
      return $candidate
    }
  }

  throw "Could not find a free port for $Label in range $RequestedPort..$($RequestedPort + 50)."
}

function Wait-ForPortListening {
  param(
    [int]$Port,
    [int]$TimeoutSeconds = 60
  )

  $deadline = (Get-Date).AddSeconds($TimeoutSeconds)
  while ((Get-Date) -lt $deadline) {
    $isListening = Get-NetTCPConnection -State Listen -LocalPort $Port -ErrorAction SilentlyContinue
    if ($isListening) {
      return $true
    }
    Start-Sleep -Milliseconds 500
  }

  return $false
}

function Wait-ForBackendReady {
  param(
    [int]$Port,
    [int]$TimeoutSeconds = 90
  )

  $deadline = (Get-Date).AddSeconds($TimeoutSeconds)
  while ((Get-Date) -lt $deadline) {
    try {
      $response = Invoke-RestMethod -Uri "http://127.0.0.1:$Port/actuator/health" -Method Get -TimeoutSec 2
      if ($response -and $response.status -eq "UP") {
        return $true
      }
    } catch {
      # Ignore transient failures until timeout.
    }

    try {
      $loginPayload = @{ username = "manager"; password = "MgrD5v8cN4" } | ConvertTo-Json -Compress
      $loginResponse = Invoke-RestMethod `
        -Uri "http://127.0.0.1:$Port/api/auth/seed-login" `
        -Method Post `
        -ContentType "application/json" `
        -Body $loginPayload `
        -TimeoutSec 3
      if ($loginResponse -and $loginResponse.token) {
        return $true
      }
    } catch {
      # Ignore transient failures until timeout.
    }

    Start-Sleep -Seconds 1
  }

  return $false
}

function Get-BackendStartupDiagnosticMessage {
  param([string]$EnvFilePath)

  try {
    $backendLogs = docker compose --env-file "$EnvFilePath" logs backend --tail=200 2>&1 | Out-String
  } catch {
    return $null
  }

  if ([string]::IsNullOrWhiteSpace($backendLogs)) {
    return $null
  }

  if ($backendLogs -match "Migrations have failed validation" -or $backendLogs -match "Migration checksum mismatch") {
    return @(
      "Backend failed to start because Flyway validation detected a stale local Docker Postgres volume."
      "This usually happens after edited migrations or switching branches with an existing postgres_data volume."
      ""
      "Run: docker compose down -v"
      "Then start again: powershell -ExecutionPolicy Bypass -File .\\start-dev.ps1"
    ) -join [Environment]::NewLine
  }

  return $null
}

function Resolve-ComposeBackendPort {
  param(
    [string]$EnvFilePath,
    [int]$FallbackPort
  )

  try {
    $portOutput = & docker compose --env-file $EnvFilePath port backend 8080 2>$null | Select-Object -First 1
    if ([string]::IsNullOrWhiteSpace($portOutput)) {
      return $FallbackPort
    }
    if ($portOutput.Trim() -match ":(\d+)\s*$") {
      return [int]$Matches[1]
    }
  } catch {
    # Keep fallback when compose port cannot be resolved.
  }

  return $FallbackPort
}

function Test-DockerTransientError {
  param([string]$OutputText)

  if ([string]::IsNullOrWhiteSpace($OutputText)) {
    return $false
  }

  $patterns = @(
    "unexpected EOF",
    "TLS handshake timeout",
    "i/o timeout",
    "connection reset by peer",
    "reset by peer",
    "context canceled",
    "temporary failure in name resolution",
    "net/http: request canceled",
    "failed to do request",
    "failed to copy: httpReadSeeker",
    "no route to host",
    "received unexpected HTTP status",
    "failed to solve:.*EOF"
  )

  foreach ($pattern in $patterns) {
    if ($OutputText -match $pattern) {
      return $true
    }
  }

  return $false
}

function Invoke-CommandWithDockerRetry {
  param(
    [string]$CommandLine,
    [string]$FailureMessage,
    [int]$MaxAttempts = 3,
    [int]$TimeoutSeconds = 900,
    [switch]$SoftFail
  )

  for ($attempt = 1; $attempt -le $MaxAttempts; $attempt++) {
    Write-Host "Running: $CommandLine"
    $result = Invoke-CmdWithTimeout -CommandLine $CommandLine -TimeoutSeconds $TimeoutSeconds
    $exitCode = $result.ExitCode
    $outputText = $result.Output

    if (-not $result.TimedOut -and $exitCode -eq 0) {
      return
    }

    $isTransient = $result.TimedOut -or (Test-DockerTransientError -OutputText $outputText)
    if (-not $isTransient -or $attempt -eq $MaxAttempts) {
      if ($SoftFail) {
        if ($result.TimedOut) {
          Write-Warning "$FailureMessage timed out after $TimeoutSeconds seconds. Continuing..."
        } else {
          Write-Warning "$FailureMessage failed with exit code $exitCode. Continuing..."
        }
        return $false
      }
      if ($result.TimedOut) {
        throw "$FailureMessage timed out after $TimeoutSeconds seconds."
      }
      throw "$FailureMessage failed with exit code $exitCode."
    }

    $delaySeconds = [Math]::Min(30, [Math]::Pow(2, $attempt + 1))
    Write-Warning "Transient Docker error detected. Retry $($attempt + 1)/$MaxAttempts in $delaySeconds sec..."
    Start-Sleep -Seconds $delaySeconds
  }

  return $true
}

