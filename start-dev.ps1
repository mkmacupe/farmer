function Require-Command {
  param([string]$Name)
  if (-not (Get-Command $Name -ErrorAction SilentlyContinue)) {
    throw "Command '$Name' was not found in PATH."
  }
}

function Ensure-GitHooksConfigured {
  $gitCommand = Get-Command git -ErrorAction SilentlyContinue
  if (-not $gitCommand) {
    return
  }

  $hooksDir = Join-Path $projectRoot ".githooks"
  if (-not (Test-Path -LiteralPath $hooksDir)) {
    return
  }

  $currentHooksPath = ""
  try {
    $currentHooksPath = (& $gitCommand.Source config --get core.hooksPath 2>$null | Select-Object -First 1).Trim()
  } catch {
    $currentHooksPath = ""
  }

  if ($currentHooksPath -eq ".githooks") {
    return
  }

  & $gitCommand.Source config core.hooksPath .githooks | Out-Null
  Write-Host "Configured repository git hooks at .githooks"
}

function New-RandomSecret {
  param([int]$Bytes = 48)

  $buffer = New-Object byte[] $Bytes
  $rng = [System.Security.Cryptography.RandomNumberGenerator]::Create()
  try {
    $rng.GetBytes($buffer)
  } finally {
    if ($rng) {
      $rng.Dispose()
    }
  }
  return [Convert]::ToBase64String($buffer)
}

function New-UrlSafeSecret {
  param([int]$Bytes = 48)

  return (New-RandomSecret -Bytes $Bytes).TrimEnd('=').Replace('+', '-').Replace('/', '_')
}

function Test-PlaceholderValue {
  param([string]$Value)

  if ([string]::IsNullOrWhiteSpace($Value)) {
    return $true
  }

  $normalized = $Value.Trim().ToLowerInvariant()
  if ($normalized.StartsWith("replace-with-")) {
    return $true
  }
  if ($normalized.StartsWith("change-me")) {
    return $true
  }
  if ($normalized -eq "changeme" -or $normalized -eq "your-secret-here" -or $normalized -eq "todo") {
    return $true
  }

  return $false
}

function Read-EnvFile {
  param([string]$Path)

  $values = [ordered]@{}
  if (-not (Test-Path $Path)) {
    return $values
  }

  foreach ($line in Get-Content -LiteralPath $Path) {
    $trimmed = $line.Trim()
    if (-not $trimmed -or $trimmed.StartsWith("#")) {
      continue
    }

    $parts = $trimmed -split "=", 2
    if ($parts.Count -ne 2) {
      continue
    }

    $key = $parts[0].Trim()
    $value = $parts[1].Trim()
    if ($key) {
      $values[$key] = $value
    }
  }

  return $values
}

